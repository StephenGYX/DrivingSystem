package com.drivingsys.bean.echartstest;

public class echaretsDSC
{
	private long renshu;
	private String dname;
	private String did;
private String releaseYearMonth;

	public long getRenshu()
	{
		return renshu;
	}

	public void setRenshu(long renshu)
	{
		this.renshu = renshu;
	}

	public String getDname()
	{
		return dname;
	}

	public void setDname(String dname)
	{
		this.dname = dname;
	}

	public String getDid()
	{
		return did;
	}

	public void setDid(String did)
	{
		this.did = did;
	}

	public String getReleaseYearMonth()
	{
		return releaseYearMonth;
	}

	public void setReleaseYearMonth(String releaseYearMonth)
	{
		this.releaseYearMonth = releaseYearMonth;
	}
}
